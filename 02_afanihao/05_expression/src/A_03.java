// 口算出下表达式的值。

public class A_03 {
    public static void main(String[] args) {
        int  a = 10;
        int  b = 20;
        int  result = a * b;
        System.out.println(result);

        result = 17 % 4;
        System.out.println(result);

        result = 4 % 17;
        System.out.println(result);

        result = 17 % 17;
        System.out.println(result);

        result = 13 / 5;
        System.out.println(result);

        double result1 = 13.0 / 5.0;
        System.out.println(result1);
    }
}
