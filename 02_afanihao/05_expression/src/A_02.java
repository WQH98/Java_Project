/*
    给定3个数a,b,c ，求它们的平方之和。
        int a = 123;
        int b = 23;
        int c = 9;
    求a,b,c的平方之和。并打印输出。
*/

public class A_02 {
    public static void main(String[] args) {
        int a = 123;
        int b = 23;
        int c = 9;
        int res = a * a + b * b + c * c;
        System.out.println(res);
    }
}
